    //method to start the timer
    private void startTimer() {
        secondsPassed = 0;

        timer = new Timer(1000, new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            secondsPassed++;
            if(secondsPassed > 300)
            {
                timer.stop();
                JOptionPane.showMessageDialog(SlidePuzzle.this, "Time's Up!","Slide Puzzle",JOptionPane.INFORMATION_MESSAGE);
                disableButtons();
                jButton19.setEnabled(false);
                
            }
            else
            {
                int newValue = secondsPassed;
                 int minutes = newValue / 60;
                int seconds = newValue % 60;
                jLabel2.setText(String.format("Timer  %02d:%02d", minutes, seconds));
            }
            
        }
        });
        timer.start();
    }
    
    //method to reset the timer to 00:00
    private void resetTimer() {
        if (timer != null) {
            timer.stop();
            secondsPassed = 0;
            jLabel2.setText("Timer: 00:00");
        }
    }
    
