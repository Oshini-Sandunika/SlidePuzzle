    // Method to go back to the previous state when jButton22 is pressed
    private void goBackToPreviousState() {
        jButton19.setEnabled(false);
        jButton22.setEnabled(false);
        timer.stop();
        if (!state.isEmpty()) {
            currentIndex = currentStateIndex - 1;
            if (currentIndex < 0) {
                currentIndex = state.size() - 1; 
            }
        
        timer2 = new Timer(150, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (currentIndex >= 0) { 
                    List<String> currentState = (List<String>) state.get(currentIndex); 
                    assignStateToButtons(currentState); 
                    if (checkWinState(currentState)) {
                        ((Timer) e.getSource()).stop(); 
                        state.clear();
                        disableButtons();
                    }
                    currentIndex--; 
                } else {
                    jButton1.setText("1");
                    jButton2.setText("2");
                    jButton3.setText("3");
                    jButton4.setText("4");
                    jButton5.setText("8");
                    jButton6.setText("7");
                    jButton7.setText("6");
                    jButton8.setText("5");
                    jButton9.setText("9");
                    jButton10.setText("10");
                    jButton11.setText("11");
                    jButton12.setText("12");
                    jButton13.setText("");
                    jButton14.setText("15");
                    jButton15.setText("14");
                    jButton16.setText("13"); 
                    ((Timer) e.getSource()).stop();
                    disableButtons();
                }
            }
        });
        timer2.start();
    }
}


